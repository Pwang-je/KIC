package java_basic3.src;

public class Sawon_Dto {

  // 사번, 이름, 기본급, 입사년도.

  private String name;
  private int sabun, kibon, ipsa;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getSabun() {
    return sabun;
  }

  public void setSabun(int sabun) {
    this.sabun = sabun;
  }

  public int getKibon() {
    return kibon;
  }

  public void setKibon(int kibon) {
    this.kibon = kibon;
  }

  public int getIpsa() {
    return ipsa;
  }

  public void setIpsa(int ipsa) {
    this.ipsa = ipsa;
  }
}
