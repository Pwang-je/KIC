package java_basic3.src;

public class StudDto extends Collection {

  private String name;
  private int lang, eng;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getLang() {
    return lang;
  }

  public void setLang(int lang) {
    this.lang = lang;
  }

  public int getEng() {
    return eng;
  }

  public void setEng(int eng) {
    this.eng = eng;
  }
}
