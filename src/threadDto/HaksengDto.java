package java_basic3.src;

public class HaksengDto {
  private String name;
  private int ko, en, ma;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getKo() {
    return ko;
  }

  public void setKo(int ko) {
    this.ko = ko;
  }

  public int getEn() {
    return en;
  }

  public void setEn(int en) {
    this.en = en;
  }

  public int getMa() {
    return ma;
  }

  public void setMa(int ma) {
    this.ma = ma;
  }
}
